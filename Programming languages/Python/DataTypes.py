# Assign values to variables (to ensure independence)
name = "John" # String
age = 30 # Int
is_student = False # Bool
pi = 3.14 # Float

# Check variable types using type() function
print("Type of 'name' variable:", type(name))
print("Type of 'age' variable:", type(age))
print("Type of 'is_student' variable:", type(is_student))
print("Type of 'pi' variable:", type(pi))

# Dynamic typing: Variables can hold different types of values
dynamic_variable = "Hello"
print("Dynamic Variable:", dynamic_variable)
dynamic_variable = 123
print("Updated Dynamic Variable:", dynamic_variable)
