# Modules Python Script

# Import the math module
import math

# Calculate the square root of a number using the math module
number = 16
square_root = math.sqrt(number)
print("Square root of", number, "is:", square_root)

# Calculate the factorial of a number using the math module
factorial_result = math.factorial(5)
print("Factorial of 5 is:", factorial_result)

# Import a specific function from the math module
from math import sin, pi

# Calculate the sine of an angle in radians
angle = pi / 2
sin_result = sin(angle)
print("Sine of", angle, "radians is:", sin_result)
