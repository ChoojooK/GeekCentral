// Bitwise operators in C++ are used to perform operations on the binary representations of integers. They work directly on the bits of the numbers.

unsigned int x = 5, y = 3;
unsigned int resultAnd = (x & y);  // Bitwise AND
unsigned int resultOr = (x | y);  // Bitwise OR
unsigned int resultXor = (x ^ y);  // Bitwise XOR
unsigned int resultNotX = ~x;  // Bitwise NOT
unsigned int resultLeftShift = (x << 1);  // Left shift
unsigned int resultRightShift = (x >> 1);  // Right shift
